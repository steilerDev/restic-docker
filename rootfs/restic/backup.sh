#!/bin/sh

STATUS_FILE="/restic/status.info"

echo "###############################################################################"
echo "Backup started at $(date)" | tee $STATUS_FILE
echo "###############################################################################"
/restic/restic backup /backup
if [ $? -eq 0 ]; then
    echo "###############################################################################"
    echo "Backup successfully finished at $(date)" | tee $STATUS_FILE
    echo "###############################################################################"
    /restic/forget.sh
else
    echo "###############################################################################"
    echo "Backup failed at $(date)" | tee $STATUS_FILE
    echo "###############################################################################"
fi

